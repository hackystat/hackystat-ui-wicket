/**
 * Enumeration class of streams.
 * 
 * @author Shaoxuan Zhang
 */         
public enum StreamCategory {
  /** Indicates a increasing trend. */
  INCREASING,        
  /** Indicates a decreasing trend. */
  DECREASING,      
  /** Indicates a stable trend. */
  STABLE,
  /** Indicates a good stream. */
  GOOD,
  /** Indicates a average stream. */
  AVERAGE,
  /** Indicates a poor stream. */
  POOR,
  /** Indicates other trends. */
  OTHER,
    
}
